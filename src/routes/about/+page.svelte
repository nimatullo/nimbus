	<div>
		<h4 class="font-semibold">
			Imagine you have $100 and a bank gives you 10% interest each year:
		</h4>
		<ul>
			<li>
				<strong>Year 1:</strong> You earn $10 in interest, so you have $110.
			</li>
			<li>
				<strong>Year 2:</strong> You earn interest on $110, which is $11, so you have $121.
			</li>
			<li>
				<strong>Year 3:</strong> You earn interest on $121, which is $12.10, so you have $133.10.
			</li>
		</ul>
		<p>
			Each year your money grows faster because you're earning interest not just on what you started
			with, but also the interest you've already earned.
